	public int canCompleteCircuit(final List<Integer> gas, final List<Integer> cost) {

		int n;
		int remainGas = 0;
		int i;
		int min = 0;
		int lastPos = 0;

		n = gas.size();

		for (i = 0; i < n; i++) {
			remainGas += gas.get(i);
			remainGas -= cost.get(i);

			lastPos = Math.max(remainGas, gas.get(i) - cost.get(i) + lastPos);

			if (lastPos >= 0) {
				if (min == -1)
					min = i;
			} else {
				min = -1;
			}

			lastPos = Math.max(0, lastPos);

		}

		if (remainGas < 0)
			return -1;


		return min;


	}
